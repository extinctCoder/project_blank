def sum(number_one: float = 0, number_two: float = 0) -> float:
    '''This function return the sum value of two numbers

    Args:
        number_one (float, optional): Number One. Defaults to 0.
        number_two (float, optional): Number Two. Defaults to 0.

    Returns:
        float: The sum value.
    '''
    return number_one + number_two


def sub(number_one: float = 0, number_two: float = 0) -> float:
    '''This function returns the sub value of two numbers.

    Args:
        number_one (float, optional): Number One. Defaults to 0.
        number_two (float, optional): Number Two. Defaults to 0.

    Returns:
        float: The sub value.
    '''
    return number_one - number_two
